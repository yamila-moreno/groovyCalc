class Calc {
    Integer res = 0

    Integer add(Integer n) {
        res = res + n
        return res
    }

    Integer sub(Integer n) {
        res = res - n
        return res
    }

    Integer prod(Integer n) {
        res = res * n
        return res
    }

    Integer div(Integer n) {
        res = res / n
        return res
    }

}
